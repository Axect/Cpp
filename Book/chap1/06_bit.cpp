#include <iostream>

using namespace std;

int main() {
    const int x = 2;

    cout << ~x << '\n'          // -3 (0x00000002 -> 0xfffffffd = -3)
         << (x << 3) << '\n'    // 16 (0b...10 -> 0b...10000)
         << (x >> 1) << '\n'    // 1  (0b...10 -> 0b...01)
         << (x & 1) << '\n'     // 0  (0b...10 & 0b...01 -> 0b000..000)
         << (x ^ 6) << '\n'     // 4  (0b...010 ^ 0b...110 -> 0b...100)
         << (x | 5) << endl;    // 7  (0b...010 | 0b...101 -> 0b...111)
}
